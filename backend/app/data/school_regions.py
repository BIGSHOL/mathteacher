def get_school_region(school_name: str) -> tuple[str | None, str | None, str | None]:
    """Get region info for a school name.

    Args:
        school_name: Name of the school (e.g., "경신중학교")

    Returns:
        Tuple of (city, district, school_type) or (None, None, None) if not found
    """
    # 정확한 매칭
    if school_name in SCHOOL_REGION_MAP:
        return SCHOOL_REGION_MAP[school_name]

    # "고등학교" -> "고" 등의 축약형 처리
    normalized = school_name
    for suffix in ["고등학교", "중학교", "초등학교"]:
        if normalized.endswith(suffix):
            break
    else:
        # 축약형 시도
        if normalized.endswith("고"):
            normalized = normalized[:-1] + "고등학교"
        elif normalized.endswith("중"):
            normalized = normalized[:-1] + "중학교"

        if normalized in SCHOOL_REGION_MAP:
            return SCHOOL_REGION_MAP[normalized]

    return None, None, None


def format_school_region(city: str | None, district: str | None) -> str | None:
    """Format region string for storage.

    Args:
        city: City name (e.g., "대구광역시")
        district: District name (e.g., "수성구")

    Returns:
        Formatted region string (e.g., "대구_수성구") or None
    """
    if not city:
        return None

    # 광역시/도 약칭
    city_short = city.replace("광역시", "").replace("특별시", "").replace("도", "")

    if district:
        return f"{city_short}_{district}"
    return city_short


